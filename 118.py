# Вычислить число пи  c заданной точностью d
# 	Пример: при d = 0.001,  = 3.141. 10-1d10-10

d= float(input())

t = 10000

# пи = 3 + 4/(2*3*4) - 4/(4*5*6) + 4/(6*7*8) - 4/(8*9*10) + 4/(10*11*12) - 4/(12*13*14)

znak = 1

f=3
g=2
while t > 0:
    f+=(4/(g*(g+1)*(g+2)))*znak
    g+=2
    t-=1
    znak*=-1

d = str(d)

d=len(d)-2

f = round(f,d)

print(f)

